package network.reborn.core.Module.Games;

/**
 * PVP_1_9 - Default 1.9 PVP with attack speed and also off hand support etc
 * PVP_1_8 - 1.8 PVP (no attack speed) but still have 1.9 features like off hand etc
 * TRUE_1_8 - 1.8 PVP but no 1.9 features, no off hand, gapples are as close to 1.8 as possible, blocking etc
 */
public enum PVPMode {
    PVP_1_9, PVP_1_8, TRUE_1_8
}